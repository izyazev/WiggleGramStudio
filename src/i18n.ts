export type AppLanguage = "ru" | "en";

export function detectSystemLanguage(): AppLanguage {
  const candidate = (navigator.languages?.[0] ?? navigator.language ?? "en").toLowerCase();
  return candidate.startsWith("ru") ? "ru" : "en";
}

export function isCancelledMessage(message: string): boolean {
  const normalized = message.toLowerCase();
  return normalized.includes("отмен") || normalized.includes("cancel");
}

